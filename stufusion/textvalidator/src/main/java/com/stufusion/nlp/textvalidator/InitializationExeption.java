package com.stufusion.nlp.textvalidator;

public class InitializationExeption extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -9166704673990136106L;

	public InitializationExeption() {
		super();
	}

	public InitializationExeption(String message, Throwable cause) {
		super(message, cause);

	}

	public InitializationExeption(String message) {
		super(message);

	}

	public InitializationExeption(Throwable cause) {
		super(cause);

	}

}
